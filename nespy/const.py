# CPU status register
C = (1 << 0) # carry
Z = (1 << 1) # zero
I = (1 << 2) # interrupt disable
D = (1 << 3) # decimal
B = (1 << 4) # break
U = (1 << 5) # unused
V = (1 << 6) # overflow
N = (1 << 7) # negative

